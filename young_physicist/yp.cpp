#include<iostream>


using namespace std;

int main(){
	int n;
	cin >> n;

	int x, y, z;
	cin >> x >> y >>z;
	int xi, yi, zi;
	for (int i = 0; i < n-1; i++) {
		cin >> xi >> yi >> zi;
		x += xi;
		y += yi;
		z += zi;
	}

	if (x == 0 && y == 0 && z == 0) {
		cout << "YES";
	}
	else {
		cout << "NO";
	}
}
