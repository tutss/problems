const int MAX = 1001;

int lcs(string s, string t){
    int m = int(s.size()); int n = int(t.size());
    int dp[MAX][MAX]; fill(*dp, *dp + (m+1)*(n+1), 0);

    for (int i=1; i<=m; i++){
        for (int j=1; j<=n; j++){
            if (s[i-1]==t[j-1]) dp[i][j] = dp[i-1][j-1]+1;
            else dp[i][j] = max(dp[i-1][j], dp[i][j-1]);
        }
    }

    return dp[m][n];
}

